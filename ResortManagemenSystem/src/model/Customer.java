package model;

/**
 *
 * @author Yashitha nadiranga <your.name at your.org>
 */
public class Customer {
    private String cid;
    private String fname;
    private String lname;
    private String nic;
    private String addr;
    private String tp;
    private String email;

    public Customer() {
    }

    public Customer(String cid, String fname, String lname, String nic, String addr, String tp, String email) {
        this.cid = cid;
        this.fname = fname;
        this.lname = lname;
        this.nic = nic;
        this.addr = addr;
        this.tp = tp;
        this.email = email;
    }
    
    

    /**
     * @return the cid
     */
    public String getCid() {
        return cid;
    }

    /**
     * @param cid the cid to set
     */
    public void setCid(String cid) {
        this.cid = cid;
    }

    /**
     * @return the fname
     */
    public String getFname() {
        return fname;
    }

    /**
     * @param fname the fname to set
     */
    public void setFname(String fname) {
        this.fname = fname;
    }

    /**
     * @return the lname
     */
    public String getLname() {
        return lname;
    }

    /**
     * @param lname the lname to set
     */
    public void setLname(String lname) {
        this.lname = lname;
    }

    /**
     * @return the nic
     */
    public String getNic() {
        return nic;
    }

    /**
     * @param nic the nic to set
     */
    public void setNic(String nic) {
        this.nic = nic;
    }

    /**
     * @return the addr
     */
    public String getAddr() {
        return addr;
    }

    /**
     * @param addr the addr to set
     */
    public void setAddr(String addr) {
        this.addr = addr;
    }

    /**
     * @return the tp
     */
    public String getTp() {
        return tp;
    }

    /**
     * @param tp the tp to set
     */
    public void setTp(String tp) {
        this.tp = tp;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Customer{" + "cid=" + cid + ", fname=" + fname + ", lname=" + lname + ", nic=" + nic + ", addr=" + addr + ", tp=" + tp + ", email=" + email + '}';
    }
    
    
}
